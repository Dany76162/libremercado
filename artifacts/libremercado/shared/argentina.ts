export function getProvinciaById(id: string): Provincia | undefined {
  return provincias.find((p) => p.id === id);
}

export function getCiudadById(provinciaId: string, ciudadId: string): Ciudad | undefined {
  const provincia = getProvinciaById(provinciaId);
  return provincia?.ciudades.find((c) => c.id === ciudadId);
}

export function getLocationName(provinciaId: string, ciudadId?: string): string {
  const provincia = getProvinciaById(provinciaId);
  if (!provincia) return "Seleccionar ubicación";
  
  if (ciudadId) {
    const ciudad = getCiudadById(provinciaId, ciudadId);
    if (ciudad) return `${ciudad.name}, ${provincia.name}`;
  }
  
  return provincia.name;
}

export function getDivisionLabel(provincia: Provincia): string {
  return provincia.divisionTipo || "Localidad";
}
